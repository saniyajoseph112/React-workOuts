import { createSlice } from "@reduxjs/toolkit";

const UseSlice = createSlice({
  name: "user",
  initialState: {
    user: null,
  },
  reducers: {
    login: (state, action) => {
      state.user = action.payload;
    },
    logout: (state) => {
      state.user = null;
    },
  },
});

export const { login, logout } = UseSlice.actions;
export default UseSlice.reducer;
